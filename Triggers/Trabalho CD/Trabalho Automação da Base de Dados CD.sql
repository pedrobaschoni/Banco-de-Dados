DELIMITER $$
create procedure gerarVenda(quant int)
deterministic
BEGIN
	declare i int default 1;
	declare parcelas int default 1;
    declare contador int default 1;
    
    while i <= quant do
		insert into venda values (i, now(), contador, parcelas);
		set parcelas = parcelas + 1;
        set contador = contador + 1;
        
        if contador > 100 then
			set contador = 1;
		end if;
        
        if parcelas > 100 then
			set parcelas = 1;
		end if;
        
        set i = i + 1;
    end while;
END $$
DELIMITER ;

call gerarVenda(1000);



