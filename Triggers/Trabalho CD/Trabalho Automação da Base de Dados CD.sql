create view lucroauto as
	with precomusica as (
		select faixa.codcd, c.preco/count(*) as totmusica from faixa
        join cd c on (c.codcd = faixa.codcd)
	group by faixa.codcd
)

select ma.codaut, f.codcd, count(ma.codmus) * totmusica as total, 10000- c.qtdeestoque as vendido from musicaautor ma
    join faixa f on (f.codmus = ma.codmus)
    join precomusica p on (f.codcd = p.codcd)
    join cd c on (c.codcd = f.codcd)
group by ma.codaut, f.codcd
order by ma.codaut;
    
select * from lucroauto;